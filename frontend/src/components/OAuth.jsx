import { getAuth, GoogleAuthProvider, signInWithPopup } from 'firebase/auth';
import { app } from "../firebase.js";
import { useDispatch } from 'react-redux';
import { useNavigate } from 'react-router-dom';
import { ArrowRight } from "lucide-react";
import { toast } from 'sonner';
import { setUser } from '@/redux/userSlice.js';
import { useState } from 'react';

export default function OAuth() {
    const dispatch = useDispatch();
    const navigate = useNavigate();

    const [loading, setLoading] = useState(false);

    const handleGoogleClick = async () => {
        if (loading) return;
        setLoading(true);

        try {
            const provider = new GoogleAuthProvider();
            const auth = getAuth(app);

            const result = await signInWithPopup(auth, provider);

            const res = await fetch("/api/user/google", {
                method: "POST",
                headers: { "Content-Type": "application/json" },
                credentials: "include",
                body: JSON.stringify({
                    name: result.user.displayName,
                    email: result.user.email,
                    photo: result.user.photoURL,
                }),
            });

            const data = await res.json();

            if (!res.ok) {
                throw new Error(data.message || "Google login failed");
            }

            dispatch(setUser(data.user));
            toast.success("Logged in with Google");
            navigate("/");
        } catch (error) {
            console.error(error);
            toast.error(error.message || "Google login failed");
        } finally {
            setLoading(false);
        }
    };

    return (
        <button
            type="button"
            disabled={loading}
            onClick={handleGoogleClick}
            className="w-full flex items-center justify-center gap-4 bg-white border border-slate-200 py-3 px-6 rounded-full hover:bg-slate-50 hover:border-slate-300 transition-all active:scale-[0.98] shadow-sm group"
        >
            {/* Google Icon */}
            <svg width="20" height="20" viewBox="0 0 24 24">
                <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4" />
                <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l3.66-2.84z" fill="#FBBC05" />
                <path d="M12 5.38c1.62 0 3.06.56 4.21 1.66l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
            </svg>

            <span className="text-slate-700 font-bold">
                Sign in with Google
            </span>

            <ArrowRight className="group-hover:translate-x-1 transition-all" />
        </button>
    );
}
